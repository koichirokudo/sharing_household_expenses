enum CancelReason {
  expensive, // 料金が高い
  featuresNotNeeded, // 提供される機能が不要だった
  difficultToUse, // 利用方法が難しかった
  tooManyBugs, // バグが多い
  dissatisfiedWithPerformance, // パフォーマンスに不満
  providerService, // 代わりのサービスを見つけた
  temporaryUse, // 一時的に利用したかっただけ
  privacyConcerns, // プライバシーとセキュリティの懸念
  others, // その他（自由記入欄）
}
